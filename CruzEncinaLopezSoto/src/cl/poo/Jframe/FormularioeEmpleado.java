    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed
        
        
        boolean isRutValido = ValidadorRut.validarRut(txtRut.getText());
        
        if(isRutValido){
            
            empleado.setRut(txtRut.getText());
            lblValidadorRut.setText("");   
        }else{
            lblValidadorRut.setText("ERROR RUT 'XXXXXXXX'");
        }
        
        boolean isDvValido = ValidadorDv.validarDV(txtDv.getText());
        
        if(isDvValido) {
            empleado.setDv(txtDv.getText());
            lblValidadorDv.setText("");
        }else{
            lblValidadorDv.setText("ERROR DV 'X'");
        }
        
        
        String rut = txtRut.getText();
        
        String dv = txtDv.getText();

        EmpleadoDAO dao = new EmpleadoDAO();
        boolean resultado = dao.borrar(rut,dv);
        
        System.out.println("BORRAR ====> " + resultado);
        
        if(resultado && isRutValido && isDvValido){
            
            JOptionPane.showMessageDialog(rootPane, "USUARIO ELIMINADO CON EXITO");            
            limpiar();
        }else{
            JOptionPane.showMessageDialog(rootPane, "NO SE HA PODIDO ELIMINAR EL USUARIO");                        
        } 
        
            
            
        
    }//GEN-LAST:event_btnBorrarActionPerformed

