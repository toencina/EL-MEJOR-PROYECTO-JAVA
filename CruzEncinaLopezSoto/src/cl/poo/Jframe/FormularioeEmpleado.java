    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        
        boolean isRutValido = ValidadorRut.validarRut(txtRut.getText());
        
        
        
        if(isRutValido){
            
            empleado.setRut(txtRut.getText());
            lblValidadorRut.setText("");   
        }else{
            lblValidadorRut.setText("ERROR DEBE INGRESAR NUMERICO ENTRE 7 Y 8");
        }
        
        boolean isDvValido = ValidadorDv.validarDV(txtDv.getText());
        
        if(isDvValido) {
            empleado.setDv(txtDv.getText());
            lblValidadorDv.setText("");
        }else{
            lblValidadorDv.setText("ERROR DV");
        }
        
        boolean isNombreValido = false;
        
        try{
            ValidadorLargo.validarLargoTexto(txtNombre.getText(), 1, 60, "NOMBRES");
            empleado.setNombres(txtNombre.getText());
            lblValidadorNombre.setText("");
            isNombreValido = true;
        }catch(Exception ex){
            lblValidadorNombre.setText("ERROR...CAMPO OBLIGATORIO");
            lblValidadorNombre.setToolTipText(ex.getMessage());
        }
        
        boolean isApellidoPaternoValido = false;
        
        try{
            ValidadorLargo.validarLargoTexto(txtApellido1.getText(), 1, 60, "APELLIDO PATERNO");
            empleado.setApellidoPaterno(txtApellido1.getText());
            lblValidadorApellido1.setText("");
            isApellidoPaternoValido = true;
        }catch(Exception ex) {
            lblValidadorApellido1.setText("ERROR...CAMPO OBLIGATORIO");
            lblValidadorApellido1.setToolTipText(ex.getMessage());
        }
        
        boolean isApellidoMaternoValido = false;
        
        try{
            ValidadorLargo.validarLargoTexto(txtApellido2.getText(), 1, 60, "APELLIDO MATERNO");
            empleado.setApellidoMaterno(txtApellido2.getText());
            lblValidadorApellido2.setText("");
            isApellidoMaternoValido = true;
        }catch(Exception ex) {
            lblValidadorApellido2.setText("ERROR...CAMPO OBLIGATORIO");
            lblValidadorApellido2.setToolTipText(ex.getMessage());
        }
        
        boolean isTelefonoValido = false;
        
        try{
            ValidadorTelefono.validarInt(Integer.valueOf(txtTelefono.getText()),"TELEFONO");
            empleado.setTelefono(Integer.valueOf(txtTelefono.getText()));
            lblValidadorTelefono.setText("");
            isTelefonoValido = true;//FALTABA
        }catch(Exception ex){
            
            lblValidadorTelefono.setText("DEBE INGRESAR NUMERO DE 9 DIGITOS");
            lblValidadorTelefono.setToolTipText(ex.getMessage());
        }
        
        empleado.getTipoEmpleado().setNombreTipoEmpleado(cmbTipoEmpleado1.getSelectedItem().toString());///ES cmbTipoEmpleado1
        empleado.getTipoEmpleado().setCodigo(cmbTipoEmpleado1.getSelectedIndex()+1);
        
        if(isRutValido && isDvValido && isNombreValido && isApellidoPaternoValido && isApellidoMaternoValido && isTelefonoValido){
            empleado.imprimir();
        
        
        EmpleadoDAO dao = new EmpleadoDAO();
        boolean resultado = dao.insertar(empleado);
        
        System.out.println("insertado  ==>" + resultado);
        
        if(resultado){
            
            JOptionPane.showMessageDialog(rootPane, "USUARIO INGRESADO CORRECTAMENTE");
            limpiar();
            Crear();
        }else{
            JOptionPane.showMessageDialog(rootPane, "EL USUARIO NO SE HA PODIDO REGISTRAR");
        }
    }
        
        
        
        
        
        
        
    }//GEN-LAST:event_btnGuardarActionPerformed

