    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        
        String rut = txtRut.getText();
        String dv = txtDv.getText();
        //Data Access Object
        EmpleadoDAO dao = new EmpleadoDAO();
        Empleado emp = dao.buscarPorRut(rut, dv);
        
        if(emp != null){
            accionBuscar();
            txtNombre.setText(emp.getNombres());
            txtApellido1.setText(emp.getApellidoPaterno());
            txtApellido2.setText(emp.getApellidoMaterno());
            txtTelefono.setText(String.valueOf(emp.getTelefono()));
            emp.getTipoEmpleado().setNombreTipoEmpleado(cmbTipoEmpleado1.getSelectedItem().toString());
            emp.getTipoEmpleado().setCodigo(cmbTipoEmpleado1.getSelectedIndex()+1);

        }else{
            JOptionPane.showMessageDialog(rootPane, "El usuario no se encuentra registrado");
        } 
    }//GEN-LAST:event_btnBuscarActionPerformed

